def _make_backbone(name: str, pretrained: bool):
    """Returns (backbone_module, feature_dim)"""
    weights_map = {
        "resnet50":        models.ResNet50_Weights.IMAGENET1K_V1,
        "resnet101":       models.ResNet101_Weights.IMAGENET1K_V1,
        "efficientnet_b3": models.EfficientNet_B3_Weights.IMAGENET1K_V1,
        "efficientnet_b4": models.EfficientNet_B4_Weights.IMAGENET1K_V1,
    }
    if name not in weights_map:
        raise ValueError(f"Unknown backbone '{name}'. "
                         f"Choose from: {list(weights_map.keys())}")

    weights = weights_map[name] if pretrained else None

    if name.startswith("resnet"):
        try:
            resnet = getattr(models, name)(weights=weights)
        except Exception as err:
            print(f"[WARN] Could not load pretrained {name} weights ({err}); using random init.")
            resnet = getattr(models, name)(weights=None)
        feat_dim = resnet.fc.in_features
        backbone = nn.Sequential(*list(resnet.children())[:-1], nn.Flatten(1))
    else:   # EfficientNet
        try:
            effnet = getattr(models, name)(weights=weights)
        except Exception as err:
            print(f"[WARN] Could not load pretrained {name} weights ({err}); using random init.")
            effnet = getattr(models, name)(weights=None)
        feat_dim = effnet.classifier[1].in_features
        backbone = nn.Sequential(effnet.features, effnet.avgpool, nn.Flatten(1))

    return backbone, feat_dim


class PoseResNet(nn.Module):

    def __init__(self, model_cfg: dict):
        super().__init__()
        backbone_name = model_cfg.get("backbone",    "resnet50")
        pretrained    = model_cfg.get("pretrained",  True)
        head_hidden   = model_cfg.get("head_hidden", 512)
        dropout       = model_cfg.get("dropout",     0.3)

        self.backbone, feat_dim = _make_backbone(backbone_name, pretrained)

        self.head = nn.Sequential(
            nn.Linear(feat_dim, head_hidden),
            nn.BatchNorm1d(head_hidden),
            nn.ReLU(inplace=True),
            nn.Dropout(dropout),
            nn.Linear(head_hidden, 7),
        )

    def forward(self, x):
        return self.head(self.backbone(x))


