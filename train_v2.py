def build_optimiser(name: str, params, opt_cfg: dict):
    lr  = float(opt_cfg.get("lr",           1e-4))
    wd  = float(opt_cfg.get("weight_decay", 1e-4))

    if name == "adamw":
        return torch.optim.AdamW(params, lr=lr, weight_decay=wd)
    elif name == "adam":
        return torch.optim.Adam(params, lr=lr, weight_decay=wd)
    elif name == "sgd":
        return torch.optim.SGD(
            params, lr=lr, weight_decay=wd,
            momentum=float(opt_cfg.get("momentum", 0.9)),
            nesterov=bool(opt_cfg.get("nesterov", True)),
        )
    else:
        raise ValueError(f"Unknown optimiser '{name}'")


# =============================================================================
# SCHEDULER FACTORY
# =============================================================================

def build_scheduler(
    name: str, optimizer, sched_cfg: dict, total_epochs: int
) -> tuple[SchedulerType, SchedulerMode]:
    if name == "cosine_warmup":
        warmup = int(sched_cfg.get("warmup_epochs", 3))
        min_lr = float(sched_cfg.get("min_lr",       1e-6))

        def lr_lambda(epoch):
            if epoch < warmup:
                return (epoch + 1) / warmup
            progress = (epoch - warmup) / max(1, total_epochs - warmup)
            return min_lr / optimizer.param_groups[0]["initial_lr"] + \
                   (1 - min_lr / optimizer.param_groups[0]["initial_lr"]) * \
                   0.5 * (1 + np.cos(np.pi * progress))

        # Store initial_lr so the lambda can reference it
        for pg in optimizer.param_groups:
            pg["initial_lr"] = pg["lr"]

        return torch.optim.lr_scheduler.LambdaLR(optimizer, lr_lambda), "epoch"

    elif name == "plateau":
        return torch.optim.lr_scheduler.ReduceLROnPlateau(
            optimizer, mode="min",
            patience=int(sched_cfg.get("patience", 3)),
            factor=float(sched_cfg.get("factor",   0.5)),
        ), "plateau"

    elif name == "step":
        return torch.optim.lr_scheduler.StepLR(
            optimizer,
            step_size=int(sched_cfg.get("step_size", 10)),
            gamma=float(sched_cfg.get("gamma",       0.5)),
        ), "epoch"

    elif name == "none":
        return None, "none"

    else:
        raise ValueError(f"Unknown scheduler '{name}'")


