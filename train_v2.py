def main(config_path: str):
    cfg = load_config(config_path)
    required_paths = [
        str(cfg_get(cfg, "paths", "csv_path", default="")),
        str(cfg_get(cfg, "paths", "bbox_path", default="")),
        str(cfg_get(cfg, "paths", "img_dir", default="")),
    ]
    for required_path in required_paths:
        if not required_path or not os.path.exists(required_path):
            raise FileNotFoundError(f"Required path does not exist: {required_path}")

    # Seeds
    seed = int(cfg_get(cfg, "dataset", "seed", default=42))
    torch.manual_seed(seed)
    np.random.seed(seed)

    matmul_precision = str(cfg_get(cfg, "training", "matmul_precision", default="high"))
    allow_tf32 = bool(cfg_get(cfg, "training", "allow_tf32", default=True))
    cudnn_benchmark = bool(cfg_get(cfg, "training", "cudnn_benchmark", default=True))
    deterministic = bool(cfg_get(cfg, "training", "deterministic", default=False))
    if torch.cuda.is_available():
        torch.backends.cuda.matmul.allow_tf32 = allow_tf32
        torch.backends.cudnn.allow_tf32 = allow_tf32
        torch.backends.cudnn.benchmark = cudnn_benchmark and not deterministic
        torch.backends.cudnn.deterministic = deterministic
    if hasattr(torch, "set_float32_matmul_precision"):
        torch.set_float32_matmul_precision(matmul_precision)

    print("\n===== RAPTOR 6D Pose Training =====\n")

    # ── Datasets ──────────────────────────────────────────────────────────────
    full_train = SatellitePoseDataset(cfg, train=True)

    # We build one dataset and split; augmentation is applied based on the
    # train flag stored inside each subset wrapper below.
    combined = SatellitePoseDataset.__new__(SatellitePoseDataset)
    combined.__dict__.update(full_train.__dict__)  # share arrays

    val_split  = float(cfg_get(cfg, "dataset", "val_split", default=0.8))
    n_train    = int(val_split * len(combined))
    n_val      = len(combined) - n_train
    g          = torch.Generator().manual_seed(seed)

    train_ds, val_ds = random_split(combined, [n_train, n_val], generator=g)

    # Disable augmentation on val by patching the dataset's augment attr
    combined_val = SatellitePoseDataset.__new__(SatellitePoseDataset)
    combined_val.__dict__.update(full_train.__dict__)
    combined_val.augment = None
    val_ds = torch.utils.data.Subset(combined_val, val_ds.indices)

    dl_cfg    = cfg_get(cfg, "dataloader", default={})
    num_workers     = int(dl_cfg.get("num_workers",     6))
    prefetch_factor = int(dl_cfg.get("prefetch_factor", 4))
    pin_memory      = bool(dl_cfg.get("pin_memory",     True))
    batch_size      = int(cfg_get(cfg, "training", "batch_size", default=64))

    if num_workers > 0:
        train_loader = DataLoader(
            train_ds,
            batch_size=batch_size,
            shuffle=True,
            num_workers=num_workers,
            pin_memory=pin_memory,
            persistent_workers=True,
            prefetch_factor=prefetch_factor,
        )
        val_loader = DataLoader(
            val_ds,
            batch_size=batch_size,
            shuffle=False,
            num_workers=num_workers,
            pin_memory=pin_memory,
            persistent_workers=True,
            prefetch_factor=prefetch_factor,
        )
    else:
        train_loader = DataLoader(
            train_ds,
            batch_size=batch_size,
            shuffle=True,
            num_workers=0,
            pin_memory=pin_memory,
            persistent_workers=False,
        )
        val_loader = DataLoader(
            val_ds,
            batch_size=batch_size,
            shuffle=False,
            num_workers=0,
            pin_memory=pin_memory,
            persistent_workers=False,
        )

    print(f"[Loader] Train batches: {len(train_loader)}  "
          f"Val batches: {len(val_loader)}")

    # ── Device ────────────────────────────────────────────────────────────────
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"[Device] {device}")

    # ── Model ─────────────────────────────────────────────────────────────────
    model: nn.Module = PoseResNet(cfg_get(cfg, "model", default={})).to(device)

    if cfg_get(cfg, "model", "compile", default=True) and hasattr(torch, "compile"):
        print("[Model] Compiling with torch.compile …")
        try:
            model = cast(nn.Module, torch.compile(model))
        except Exception as err:
            print(f"[WARN] torch.compile failed ({err}); continuing without compile.")

    total_params = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"[Model] Trainable parameters: {total_params:,}")

    # ── Loss ─────────────────────────────────────────────────────────────────
    beta      = float(cfg_get(cfg, "loss", "beta", default=5.0))
    trans_loss_type = str(cfg_get(cfg, "loss", "trans_loss_type", default="smooth_l1"))
    criterion = PoseLoss(beta=beta, trans_loss_type=trans_loss_type)

    # ── Optimiser ─────────────────────────────────────────────────────────────
    opt_cfg   = cfg_get(cfg, "optimiser", default={})
    opt_name  = opt_cfg.get("name", "adamw")
    optimizer = build_optimiser(opt_name, model.parameters(), opt_cfg)

    # ── Scheduler ─────────────────────────────────────────────────────────────
    epochs     = int(cfg_get(cfg, "training", "epochs", default=50))
    sched_cfg  = cfg_get(cfg, "scheduler", default={})
    sched_name = sched_cfg.get("name", "cosine_warmup")
    scheduler, sched_mode = build_scheduler(sched_name, optimizer, sched_cfg, epochs)

    # ── AMP Scaler ────────────────────────────────────────────────────────────
    mixed_precision = bool(cfg_get(cfg, "training", "mixed_precision", default=True))
    amp_dtype = torch.float16 if device.type == "cuda" else torch.bfloat16
    use_amp = mixed_precision and (device.type in {"cuda", "cpu"})
    scaler = GradScaler(
        device=device.type,
        enabled=(use_amp and device.type == "cuda" and amp_dtype == torch.float16),
    )

    # ── TensorBoard ───────────────────────────────────────────────────────────
    log_dir = cfg_get(cfg, "paths", "log_dir", default="runs")
    writer  = SummaryWriter(log_dir=log_dir)
    print(f"[TensorBoard] Logs → {log_dir}   (run: tensorboard --logdir={log_dir})")

    # ── Resume ────────────────────────────────────────────────────────────────
    start_epoch   = 0
    best_val_loss = float("inf")
    ckpt_load     = cfg_get(cfg, "paths", "checkpoint_load")

    if ckpt_load and os.path.exists(ckpt_load):
        print(f"[Checkpoint] Resuming from {ckpt_load} …")
        start_epoch, best_val_loss = load_checkpoint(
            ckpt_load, model, optimizer, scheduler, scaler, device)
        print(f"[Checkpoint] Epoch {start_epoch}, best_val={best_val_loss:.6f}")

    # ── Early stopping ────────────────────────────────────────────────────────
    es_cfg     = cfg_get(cfg, "early_stopping", default={})
    es_enabled = bool(es_cfg.get("enabled", True))
    stopper    = EarlyStopping(
        patience  = int(es_cfg.get("patience",  10)),
        min_delta = float(es_cfg.get("min_delta", 1e-5)),
    ) if es_enabled else None

    # ── Training loop ─────────────────────────────────────────────────────────
    grad_clip   = float(cfg_get(cfg, "training", "grad_clip",   default=1.0))
    accum_steps = int(cfg_get(cfg,   "training", "accum_steps", default=1))
    skip_nonfinite_batches = bool(
        cfg_get(cfg, "training", "skip_nonfinite_batches", default=True)
    )
    oom_skip_batches = bool(cfg_get(cfg, "training", "oom_skip_batches", default=True))
    ckpt_save   = cfg_get(cfg, "paths", "checkpoint_save", default="last_checkpoint.pth")
    best_path   = cfg_get(cfg, "paths", "best_model_path", default="best_pose_model.pth")

    print(f"\n[Training] Starting — epochs {start_epoch+1} → {epochs}\n")

    for epoch in range(start_epoch, epochs):
        t0 = time.time()

        train_loss = run_epoch(
            model, train_loader, criterion, optimizer, scaler, device,
            train=True, grad_clip=grad_clip, accum_steps=accum_steps,
            mixed_precision=use_amp, amp_dtype=amp_dtype,
            skip_nonfinite_batches=skip_nonfinite_batches,
            oom_skip_batches=oom_skip_batches,
        )

        val_loss = run_epoch(
            model, val_loader, criterion, optimizer, scaler, device,
            train=False, grad_clip=grad_clip, accum_steps=accum_steps,
            mixed_precision=use_amp, amp_dtype=amp_dtype,
            skip_nonfinite_batches=skip_nonfinite_batches,
            oom_skip_batches=oom_skip_batches,
        )

        # Scheduler step
        if sched_mode == "epoch" and isinstance(
            scheduler, (torch.optim.lr_scheduler.LambdaLR, torch.optim.lr_scheduler.StepLR)
        ):
            scheduler.step()
        elif sched_mode == "plateau" and isinstance(
            scheduler, torch.optim.lr_scheduler.ReduceLROnPlateau
        ):
            scheduler.step(float(val_loss))

        current_lr = optimizer.param_groups[0]["lr"]

        # TensorBoard
        writer.add_scalar("Loss/Train",      train_loss, epoch)
        writer.add_scalar("Loss/Val",        val_loss,   epoch)
        writer.add_scalar("LR",              current_lr, epoch)

        # Save best
        if val_loss < best_val_loss:
            best_val_loss = val_loss
            # Save raw state_dict (unwrap compiled model if needed)
            raw_model = cast(nn.Module, model._orig_mod) if hasattr(model, "_orig_mod") else model
            best_dir = os.path.dirname(best_path)
            if best_dir:
                os.makedirs(best_dir, exist_ok=True)
            torch.save(raw_model.state_dict(), best_path)
            print(f"  ✓ Best model saved  (val={best_val_loss:.6f})")

        # Checkpoint
        save_checkpoint(ckpt_save, epoch, model, optimizer, scheduler, scaler, best_val_loss)

        # Logging
        mem_used, mem_total, power = get_gpu_stats()
        elapsed = time.time() - t0
        print(f"Epoch {epoch+1:>3}/{epochs}  |  "
              f"Train {train_loss:.6f}  Val {val_loss:.6f}  |  "
              f"LR {current_lr:.2e}  |  {elapsed:.1f}s")
        if mem_used is not None:
            print(f"             GPU {mem_used}/{mem_total} MB  |  "
                  f"Power {power:.1f} W")

        # Flush VRAM fragment cache
        if torch.cuda.is_available():
            torch.cuda.empty_cache()
        gc.collect()

        # Early stopping
        if stopper is not None and stopper.step(val_loss):
            print(f"\n[EarlyStopping] No improvement for {stopper.patience} epochs — stopping.")
            break

        if _interrupted:
            print("[INFO] Exiting cleanly after user interrupt.")
            break

    writer.close()
    print("\n===== Training Complete 🚀 =====")
    print(f"Best val loss : {best_val_loss:.6f}")
    print(f"Best model    : {best_path}")
    print(f"TensorBoard   : tensorboard --logdir={log_dir}")


