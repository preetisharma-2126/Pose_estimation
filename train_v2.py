class PoseAugment:
    """
    Lightweight augmentation that preserves pose label validity.
    Geometric transforms (flip, rotation) that would change the ground-truth
    pose are intentionally excluded — only photometric and mild spatial
    distortions are applied.
    """

    def __init__(self, aug_cfg: dict, img_size: int):
        self.cfg      = aug_cfg
        self.img_size = img_size
        self.enabled  = aug_cfg.get("enabled", False)

    def __call__(self, img: np.ndarray) -> np.ndarray:
        """img: float32 H×W in [0,1]"""
        if not self.enabled:
            return img

        # Brightness jitter
        bj = self.cfg.get("brightness_jitter", 0.0)
        if bj > 0 and np.random.rand() < 0.5:
            delta = np.random.uniform(-bj, bj)
            img   = np.clip(img + delta, 0.0, 1.0)

        # Contrast jitter
        cj = self.cfg.get("contrast_jitter", 0.0)
        if cj > 0 and np.random.rand() < 0.5:
            factor = np.random.uniform(1.0 - cj, 1.0 + cj)
            mean   = img.mean()
            img    = np.clip((img - mean) * factor + mean, 0.0, 1.0)

        # Small rotation (does not invalidate 6-DoF label for modest angles)
        deg = self.cfg.get("rotation_degrees", 0)
        if deg > 0 and np.random.rand() < 0.5:
            angle = np.random.uniform(-deg, deg)
            h, w  = img.shape
            M     = cv2.getRotationMatrix2D((w / 2, h / 2), angle, 1.0)
            img   = cv2.warpAffine(img, M, (w, h),
                                   flags=cv2.INTER_LINEAR,
                                   borderMode=cv2.BORDER_REFLECT_101)

        # Gaussian blur
        gb = self.cfg.get("gaussian_blur", 0.0)
        if gb > 0 and np.random.rand() < gb:
            k   = np.random.choice([3, 5])
            img = cv2.GaussianBlur(img, (k, k), 0)

        return img


