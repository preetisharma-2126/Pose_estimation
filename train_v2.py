def load_config(path: str) -> dict[str, Any]:
    with open(path, "r") as f:
        cfg = yaml.safe_load(f)
    print(f"[Config] Loaded: {path}")
    return cfg


def cfg_get(cfg: dict[str, Any], *keys, default=None) -> Any:
    """Safe nested key access: cfg_get(cfg, 'training', 'lr')"""
    obj = cfg
    for k in keys:
        if not isinstance(obj, dict) or k not in obj:
            return default
        obj = obj[k]
    return obj


