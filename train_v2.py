class SatellitePoseDataset(Dataset):

    def __init__(self, cfg: dict, train: bool = True):
        paths    = cfg["paths"]
        ds_cfg   = cfg["dataset"]
        aug_cfg  = ds_cfg.get("augmentation", {"enabled": False})

        df      = pd.read_csv(paths["csv_path"],  header=None)
        bbox_df = pd.read_csv(paths["bbox_path"], header=None)

        max_s = ds_cfg.get("max_samples")
        if max_s is not None:
            df      = df.iloc[:max_s]
            bbox_df = bbox_df.iloc[:max_s]

        # Convert to numpy once — eliminates per-sample pandas GIL overhead
        self.img_ids   = df.iloc[:, 0].to_numpy(dtype=np.int32)
        self.poses_raw = df.iloc[:, [1, 2, 3, 6, 7, 8, 9]].to_numpy(dtype=np.float32)
        self.bboxes    = bbox_df.iloc[:, [1, 2, 3, 4]].to_numpy(dtype=np.int32)

        self.img_dir  = paths["img_dir"]
        self.mask_dir = paths.get("mask_dir")
        self.img_size = int(ds_cfg.get("img_size", 224))
        self.train    = train
        self.augment  = PoseAugment(aug_cfg, self.img_size) if train else None
        # Match ImageNet normalization expected by pretrained backbones.
        self.imagenet_mean = np.array([0.485, 0.456, 0.406], dtype=np.float32)[:, None, None]
        self.imagenet_std = np.array([0.229, 0.224, 0.225], dtype=np.float32)[:, None, None]

        print(f"[Dataset] {'Train' if train else 'Val'} samples: {len(self.img_ids)}")

    def __len__(self):
        return len(self.img_ids)

    def __getitem__(self, idx):
        img_name = f"scene{self.img_ids[idx]}.png"
        img_path = os.path.join(self.img_dir, img_name)

        image = cv2.imread(img_path, cv2.IMREAD_GRAYSCALE)
        if image is None:
            print(f"[WARN] Missing: {img_path} — using black placeholder.")
            image = np.zeros((self.img_size, self.img_size), dtype=np.uint8)

        xmin, xmax, ymin, ymax = self.bboxes[idx]
        h, w = image.shape
        xmin = max(0, xmin)
        ymin = max(0, ymin)
        xmax = min(w, xmax)
        ymax = min(h, ymax)

        cropped = image[ymin:ymax, xmin:xmax]
        if cropped.size == 0:
            cropped = image

        # Optional segmentation mask
        if self.mask_dir:
            mask_path = os.path.join(self.mask_dir, img_name)
            if os.path.exists(mask_path):
                mask = cv2.imread(mask_path, cv2.IMREAD_GRAYSCALE)
                if mask is not None:
                    mask_crop = mask[ymin:ymax, xmin:xmax]
                    mask_bin  = (mask_crop > 0).astype(np.float32)
                    cropped   = (cropped.astype(np.float32) * mask_bin).astype(np.uint8)

        resized = cv2.resize(cropped, (self.img_size, self.img_size))
        img_f   = resized.astype(np.float32) / 255.0

        # Augmentation (train only)
        if self.augment is not None:
            img_f = self.augment(img_f)

        # Replicate grayscale to 3 channels, then apply ImageNet normalization.
        img_3 = np.stack([img_f, img_f, img_f], axis=0)
        img_3 = (img_3 - self.imagenet_mean) / self.imagenet_std
        image_tensor = torch.from_numpy(img_3)

        # Pose: tx,ty,tz / 100  +  quaternion normalised
        pose = self.poses_raw[idx].copy()
        pose[0:3] /= 100.0
        q_norm = np.linalg.norm(pose[3:])
        if q_norm > 1e-8:
            pose[3:] /= q_norm
        else:
            pose[3:] = np.array([0.0, 0.0, 0.0, 1.0], dtype=np.float32)

        return image_tensor, torch.from_numpy(pose)


